#include <iostream>

int main()
{

    // Greater Than: ">"
    bool result1 = 2 > 0; // true;
    bool result2 = 1 > 4; // false;

    // Less Than: "<"
    bool result3 = 2 < 0; // false
    bool result4 = 1 < 4; // true

    // Equals: "=="
    bool result5 = 2 == 0; // false
    bool result6 = 2 == 2; // true

    // Not Equals: "!="
    bool result7 = 2 != 0; // true
    bool result8 = 2 == 2; // false

    // Another Relational Operators:
    // ">=" Greater Than or Equal To
    // "<=" Less Than or Equal To

    return 0;
}